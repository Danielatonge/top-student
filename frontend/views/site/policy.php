Все материалы, так или иначе размещенные на Форуме, отражают исключительно точку зрения их авторов и/или разместивших эти материалы участников. Администрация Форума не дает никаких гарантий, что эти материалы и сообщения полны, полезны или достоверны, и не несет ответственности за их содержание. Сообщения на Форуме отражают личные мнения участников дискуссии, следует помнить, что это не рекомендации, как поступать с вашими финансовыми вложениями. Помните, что все финансовые операции, например такие, как внесение денег на вклад/изъятие денег с вклада, покупка/продажа валюты, ценных бумаг и другие, вы совершаете исключительно в соответствии со своими предпочтениями и на свой страх и риск. Поэтому перед их совершением рекомендуем вам провести самостоятельный всесторонний анализ того, что вы собираетесь сделать, с учетом ваших личных представлений об устраивающей вас потенциальной доходности и приемлемом для вас уровне риска.
